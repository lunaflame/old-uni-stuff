static double getScoreBuilt(const char x, const char y) {
	assert(XtoXY[(unsigned char)x] != -1);
	assert(XtoXY[(unsigned char)y] != -1);

	unsigned char lookup = (XtoXY[(unsigned char)y] * MATRIX_SIZE) + XtoXY[(unsigned char)x];
	//printf("(%c%c) = [%d] = %.0d\n", x, y, lookup, score_mtrx[lookup]);

	return (double)score_mtrx[lookup];
}

// i didn't come up with an another way
static double g_matchScore = 0;
static double g_diffScore = 0;

static double getScoreMatch(const char x, const char y) {
	if (x == y) {
		return g_matchScore;
	} else {
		return g_diffScore;
	}
}

int main() {
	char* seq1 = NULL;
	char* seq2 = NULL;

	char seqOk = readSequences(&seq1, &seq2);
	if (!seqOk) {
		printf("Failed to read sequences, aborting.\n");
		return 0;
	}

	double gapOpen = 0;
	double gapExt = 0;

	char gapOk = readGapPenalties(&gapOpen, &gapExt);

	if (!gapOk) {
		printf("Failed to read gap penalties, aborting.\n");
		return 0;
	}

	double matchScore = 0;
	double diffScore = 0;

	char subOk = readSubstitution(&matchScore, &diffScore);

	double (*scoringFunc) (const char x, const char y);

	if (!subOk) {
		printf("Failed to read substitution matrix, using built-in (EDNAFULL).\n");
		scoringFunc = getScoreBuilt;
	} else {
		g_matchScore = matchScore;
		g_diffScore = diffScore;
		scoringFunc = getScoreMatch;
	}

	SmithRet* smith = doTheThing(seq1, seq2, scoringFunc, gapOpen, gapExt);

	size_t sl = strlen(seq1);

	size_t s2len = strlen(seq2);
	if (s2len > sl) {
		sl = s2len;
	}

	free(seq1);
	free(seq2);

	printf("Results:\n"
		"Best score: %.2lf\n"
		"%s\n%s\n", smith->maxScore, smith->sequence1, smith->sequence2);

	freeSmithResult(smith);

	return 0;
}
